#Visualizar info. especifica:
SELECT <coluna1>, <coluna2>...
FROM <nome_tabela>

#Visualizar todas as info.:
SELECT * FROM <nome_tabela>

#Visualizar valores distintos (diferentes):
SELECT DISTINCT <coluna1>, <coluna2>...
FROM <nome_tabela>

#Visualizar a quantidade de valores diferentes de uma coluna: 
SELECT COUNT(DISTINCT <colun1>) FROM <nome_tabela>