/*
COUNT() - Vai retornar o numero de linhas que correspondem a condição especificada
AVG() - Retorna o valor médio de uma coluna numerica
SUM() - Retorna a soma de todos os valores de uma coluna numerica
*/


#Utilizando o COUNT()
SELECT COUNT(<coluna>)
FROM <nome_tabela>
WHERE <condição>

#Utilizando o AVG()
SELECT AVG(<coluna>)
FROM <nome_tabela>
WHERE <condição>

#Utilizando o SUM()
SELECT SUM(<coluna>)
FROM <nome_tabela>
WHERE <condição>

